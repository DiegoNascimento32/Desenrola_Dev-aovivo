
# O que laço de repetição while ?
# é uma estrutura de programação que repete 
# os comando que estiverem dentro do seu escopo.

# Qual sua vantangem em utilizar ?
# Principal reutilização de código.

# Qual a diferença do laço de repetição while para o for ?
# O for é usado apra percorrer listas e não tem uma condição de 
# parada obrigatoria.
# Já o while você especifica uma condição de parada.

# Para comer uma barra de chocolate.

tabletes = 6 # Contador
while tabletes > 0:
    print(f"Comendo + um tablete...agora restão {tabletes -1}")
    tabletes -= 1 # Decremento do contador


