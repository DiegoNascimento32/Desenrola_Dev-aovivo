# Operadores aritmeticos
# Somar dois numeros usamos o que ? +

idade = 20
a = 3
# Soma (+)
print(f"Idade {idade} + {a} = ", idade + a)

# Subtração (-)
print(f"Idade {idade} - {a} = ", idade - a)

# Multiplicação (*)
print(f"Idade {idade} * {a} = {idade * a}")

# Divisão (/)
print(f"Idade {idade} / {a} = ", idade / a)

# Resto da divisão (%)
print(f"Idade {idade} % {a} = {idade % a}")

# Divisão inteira (//)
print(f"Idade {idade} // {a} = {idade // a}")

# Operadores de acumulação

idade += 10 # idade = idade + 10
print("Idade += 10 = ",idade)

idade -= 10 # idade = idade - 10
print("Idade -= 10 = ",idade)

idade /= 2 # idade = idade / 2
print("Idade /= 2 = ",idade)

idade *= 2 # idade = idade * 2
print("Idade *= 2 = ",idade)

# Potencia em Python
print(f"{idade} elevado a 2 é igual:",idade ** 2)
