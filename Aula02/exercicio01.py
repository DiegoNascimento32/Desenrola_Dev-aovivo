
# O que é uma variavel ?
# É um recipiente que recebe um valor

nome = "Maria" # "" ou '' é uma string!
idade = 25 # int
altura = 1.65 # float
estudante = True # bool

print(type(nome))
print(type(idade))
print(type(altura))
print(type(estudante))

# O que é uma lista ?

alunos = ["João", "Pedro", nome, "Raimundo"]

# O que é um erro de logica ?

print(type(alunos))