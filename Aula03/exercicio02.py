
# Estrutura condicional
idade = int(input("Digite a idade: "))

# atribuição (=)
# comparação (==)


if idade == 20:
    print("Idade é igual a 20!")
elif idade > 24:
    print("Idade é maior que 24!")
else:
    print("Idade é diferente de 20!")


